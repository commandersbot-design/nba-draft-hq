const TRAIT_ORDER = [
  'Advantage Creation',
  'Decision Making',
  'Passing Creation',
  'Shooting Gravity',
  'Off-Ball Value',
  'Processing Speed',
  'Scalability',
  'Defensive Versatility',
];

const TRAIT_NOTES = {
  'Advantage Creation': 'Creates pressure mostly through a practical role-specific pathway rather than pure star-level force.',
  'Decision Making': 'Generally keeps possessions on schedule and avoids unnecessary waste.',
  'Passing Creation': 'Passing value is present, though it works more as a role-strength than an offensive engine.',
  'Shooting Gravity': 'The jumper matters to the role and will decide how cleanly the profile scales.',
  'Off-Ball Value': 'Finds ways to stay useful without needing heavy offensive ownership.',
  'Processing Speed': 'Processes quickly enough to keep both role fit and evaluative confidence intact.',
  'Scalability': 'Projects as a cleaner fit when surrounded by stronger NBA talent.',
  'Defensive Versatility': 'Defensive utility is a real part of the pathway rather than an afterthought.',
};

function makeTraits(scores) {
  return TRAIT_ORDER.map((name) => ({
    name,
    score: scores[name],
    note: TRAIT_NOTES[name],
  }));
}

function makeProfile({
  age,
  height,
  weight,
  riskLevel,
  roleProjection,
  overall,
  offense,
  defense,
  traitScores,
  synopsis,
  strengths,
  weaknesses,
  swingFactors,
  bestOutcome,
  medianOutcome,
  swingSkill,
  riskSummary,
  draftRange,
  stockBand,
}) {
  return {
    age,
    measurements: { height, weight, wingspan: '' },
    riskLevel,
    roleProjection,
    scores: {
      overallComposite: overall,
      offense,
      defense,
    },
    traits: makeTraits(traitScores),
    summary: {
      synopsis,
      strengths,
      weaknesses,
      swingFactors,
    },
    projection: {
      bestOutcome,
      medianOutcome,
      swingSkill,
      riskSummary,
      draftRange,
      stockBand,
    },
  };
}

