function formatName(name) {
  return name
    .replace(/([A-Z]{2,})([A-Z][a-z])/g, '$1 $2')
    .replace(/([a-z])([A-Z])/g, '$1 $2');
}

const prospects = rawProspects
  .trim()
  .split(/\n\s*\n/)
  .map((block, index, entries) => {
    const lines = block.split('\n').map((line) => line.trim()).filter(Boolean);
    let cursor = 0;

    const compactName = lines[cursor++];
    const height = lines[cursor++];
    const weight = /^\d+$/.test(lines[cursor] || '') ? lines[cursor++] : null;
    const position = lines[cursor++] || '';
    const school = lines[cursor++] || '';
    const classYear = lines[cursor++] || '';
    const rank = Number.parseInt(lines[cursor] || '', 10) || (index > 0 ? entries[index - 1].rank + 1 : 1);
    const movement = lines[cursor] || '0';
    const name = formatName(compactName);

    return {
      id: compactName.toLowerCase().replace(/[^a-z0-9]+/g, '-'),
      rank,
      name,
      height,
      weight,
      position,
      school,
      classYear,
      movement,
    };
  });

export default prospects;
